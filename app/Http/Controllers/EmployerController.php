     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {
        
        $emp = Employer::create(request(['companyname']));

        User::create([
            'name' => request('name'),
            'email' => request('email'),
            'password' => bcrypt(request('password')),
            'userable_id' => $emp->id,
            'userable_type' => 'employer'
        ]);

        return view('thanks');
    }

